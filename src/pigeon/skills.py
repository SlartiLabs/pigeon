"""Skill projection: one knowledge tree, every runtime's native dialect.

Canonical skill/playbook pages live once, in ``.pigeon/memory/playbooks/``,
as Markdown with YAML frontmatter::

    ---
    name: security-audit
    description: Adversarial security review of changed code.
    tools: Read, Grep, Bash        # optional, runtime-specific
    ---
    You are a security reviewer. Refuse to approve until ...

``pigeon refresh`` *projects* every page that declares a ``name`` into each
configured runtime's native subagent format — ``.claude/agents/<name>.md``
for Claude Code first (the format its Task tool discovers), more targets as
plain config. This is the AGENTS.md -> CLAUDE.md/GEMINI.md single-sourcing
pattern one level deeper: a ``crew`` entry's ``skill:`` name resolves to the
same canonical page no matter which CLI the task runs on.

Projection never clobbers hand-written files: generated output carries a
marker, and an existing file without the marker is left alone (warned).
"""

from __future__ import annotations

import re
from pathlib import Path
from typing import Any

import yaml

from .config import Config

GEN_MARKER = "<!-- generated by pigeon refresh — edit the playbook, not this file -->"

VALID_RECORD_TYPES = frozenset({"skill", "playbook", "decision", "reference"})


def parse_playbook(path: Path) -> dict[str, Any] | None:
    """Frontmatter + body of a playbook page; None when it declares no name.

    Reads optional frontmatter key ``record_type`` ∈ {skill, playbook,
    decision, reference}. Default when absent = "skill" (back-compat).
    Unknown value raises ValueError.
    """
    try:
        text = path.read_text(encoding="utf-8")
    except (OSError, UnicodeDecodeError):
        return None
    match = re.match(r"^---[ \t]*\n(.*?)\n(?:---|\.\.\.)[ \t]*\n(.*)$",
                     text, re.DOTALL)
    if not match:
        return None
    try:
        meta = yaml.safe_load(match.group(1)) or {}
    except yaml.YAMLError:
        return None
    body = match.group(2)
    if not isinstance(meta, dict) or not meta.get("name"):
        return None
    record_type = meta.get("record_type", "skill")
    if not isinstance(record_type, str) or record_type not in VALID_RECORD_TYPES:
        raise ValueError(
            f"{path}: unknown record_type {record_type!r} — "
            f"must be one of {sorted(VALID_RECORD_TYPES)}"
        )
    return {
        "name": str(meta["name"]),
        "meta": meta,
        "body": body.strip(),
        "record_type": record_type,
    }


def playbooks(config: Config) -> list[dict[str, Any]]:
    """Every projectable playbook page (has frontmatter with a name)."""
    root = config.memory_dir / "playbooks"
    found: list[dict[str, Any]] = []
    if not root.is_dir():
        return found
    for path in sorted(root.rglob("*.md")):
        parsed = parse_playbook(path)
        if parsed:
            parsed["source"] = str(path.relative_to(config.root))
            found.append(parsed)
    return found


def _render_marked(front: dict[str, Any], page: dict[str, Any]) -> str:
    """Frontmatter + GEN_MARKER provenance + the page body — the shared shape of
    every markdown-frontmatter runtime (Claude, opencode)."""
    head = yaml.safe_dump(front, sort_keys=False, allow_unicode=True).strip()
    return (f"---\n{head}\n---\n\n{GEN_MARKER}\n"
            f"<!-- source: {page['source']} -->\n\n{page['body']}\n")


def _render_claude(page: dict[str, Any]) -> str:
    """Claude Code custom-subagent format: name/description (+tools) frontmatter."""
    meta = page["meta"]
    front: dict[str, Any] = {"name": page["name"],
                             "description": meta.get("description", page["name"])}
    if meta.get("tools"):
        front["tools"] = meta["tools"]
    return _render_marked(front, page)


def _render_opencode(page: dict[str, Any]) -> str:
    """opencode subagent format (verified against opencode 1.16 agent files): a
    ``description`` + ``mode: subagent`` frontmatter, then the prompt body. The
    agent name is taken from the filename, so it is not repeated in frontmatter.
    Claude ``tools`` names don't match opencode's permission vocabulary, so they
    are intentionally not projected (opencode defaults to all permissions)."""
    meta = page["meta"]
    front: dict[str, Any] = {
        "description": meta.get("description", page["name"]),
        "mode": "subagent",
    }
    return _render_marked(front, page)


# agy / antigravity consumes Claude-format subagents verbatim (verified: its
# ~/.gemini/agents/*.md use name/description/tools frontmatter, and it symlinks
# its skills dir to ~/.claude/skills), so it reuses the Claude renderer — the
# target dir just differs (e.g. .gemini/agents). (The bare `gemini` CLI is a
# different tool with no subagent concept; not a target.)
_RENDERERS = {
    "claude": _render_claude,
    "opencode": _render_opencode,
    "agy": _render_claude,
}


def resolve_skill(config: Config, name: str) -> dict[str, Any] | None:
    """Return the canonical page for ``name`` if it is a projectable type.

    Returns the page dict (record_type in {skill, playbook}) or None.
    Used by ``crew_skill_warnings`` to distinguish known playbooks from
    unknown names without loading the full adopt catalog.
    """
    for page in playbooks(config):
        if page["name"] == name and page["record_type"] in {"skill", "playbook"}:
            return page
    return None


def project_skills(config: Config) -> dict[str, Any]:
    """Write each runtime's native subagent files from the canonical pages.

    Returns {written: [...], skipped: [...]} with repo-relative paths.
    Only pages with record_type in {skill, playbook} are projected.
    """
    targets = config.skills_cfg.get("targets", {})
    pages = [p for p in playbooks(config)
             if p["record_type"] in {"skill", "playbook"}]
    written: list[str] = []
    skipped: list[str] = []
    for runtime, target_dir in targets.items():
        renderer = _RENDERERS.get(runtime)
        if renderer is None:
            skipped.append(f"{runtime}: no renderer for this runtime yet")
            continue
        out_dir = (config.root / target_dir).resolve()
        for page in pages:
            out = out_dir / f"{page['name']}.md"
            if out.exists() and GEN_MARKER not in out.read_text(encoding="utf-8"):
                skipped.append(
                    f"{out.relative_to(config.root)}: exists and is hand-written "
                    "(no marker) — left alone"
                )
                continue
            out_dir.mkdir(parents=True, exist_ok=True)
            out.write_text(renderer(page), encoding="utf-8")
            written.append(str(out.relative_to(config.root)))
    return {"written": written, "skipped": skipped, "playbooks": len(pages)}
